
# pound to kg
user_weight = int(input("Enter your weight in pounds: "))
weight_in_kg = user_weight * 0.453592
print(f"Your weight in kg is: {weight_in_kg}")

# kg to pound
user_weight_kg = int(input("Enter your weight in kg: "))
weight_in_pounds = user_weight_kg / 0.453592
print(f"Your weight in pounds is: {weight_in_pounds}")
